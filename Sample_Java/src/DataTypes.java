

public class DataTypes 
{
	public static void main(String[] args) 
	{
		// String
		String name= "James Gosling rgdgfdg rgdgdfg dsgsdfgdfg fsdgfdgbdfgfdg";
		System.out.println("my string is "+ name);

		// Char
		char singleAlphabet = 'D';
		System.out.println("my char " + singleAlphabet);

		// integer
		int num1 = 3;
		int num2 = 5;
		int sum  = num1 + num2;
		System.out.println("sum of integers " + sum);
		
		// Double
		// float
		
		// longInt
		// long double
		double decimalValues = 3.6;
		System.out.println("My Decimal Value "+ decimalValues);
		
		// boolean (true/false) 
		boolean myflag = true;
		System.out.println("boolean value " + myflag);
		System.out.println("boolean value negation " + !myflag);

	}

}
